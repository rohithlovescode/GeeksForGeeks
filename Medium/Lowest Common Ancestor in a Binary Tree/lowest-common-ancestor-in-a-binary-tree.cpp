class Solution
{
    Node* sol=NULL;
    
    pair<bool,bool> findNodes(Node* root,int n1, int n2){
        if(root==NULL){
            return make_pair(false,false);
        }
        pair<bool,bool> foundNodes;
        if(n1==root->data){
            foundNodes.first=true;
        }
        if(n2==root->data){
            foundNodes.second=true;
        }
        pair<bool,bool> left=findNodes(root->left,n1,n2);
        pair<bool,bool> right=findNodes(root->right,n1,n2);
        if(left.first==true||right.first==true){
            foundNodes.first=true;
        }
        if(right.second==true||left.second==true){
            foundNodes.second=true;
        }
        
        if(foundNodes.first&&foundNodes.second&&!sol){
            sol=root;
        }
        return foundNodes;
        
    }
    public:
    
    Node* lca(Node* root ,int n1 ,int n2 )
    {
        pair<bool,bool> foundNodes;
        
        foundNodes=findNodes(root,n1,n2);
        
        return sol;
    }
};

